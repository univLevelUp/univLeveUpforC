//93p, 두 개의 숫자의 합을 계산하는 프로그램 
#include <stdio.h> // 전처리기

int main(void) //함수
{
    int x; // 변수 선언 (정수), int x= 10 --> 초기값 10으로 설정 
    int y; // 변수 선언 (정수)
    int sum; // 변수 선언 (정수)
    //int x, y, sum; 가능 

    x=100;
    y=200;
    //
    sum=x+y;
    printf("두수의 합: %d\n", sum);

    return 0;
}
